// 每天固定返回同一条（基于日期的伪随机）
export function getDailyInspiration(): string {
  const today = new Date()
  const seed = today.getFullYear() * 10000 + (today.getMonth() + 1) * 100 + today.getDate()
  const idx = seed % INSPIRATIONS.length
  return INSPIRATIONS[idx]
}
